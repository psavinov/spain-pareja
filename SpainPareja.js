Spain.showData = function(community) {
	var data = this.data[community];

	if (!data) {
		return;
	}

	var results = $('<div class="resultsDiv"></div>');

	var header = $('<h4>Регистрация гражданского брака в автономии <b>' + data.name + '</b></h4>');

	results.append(header);
	results.append($('<hr />'));

	var lawLink = $('<div class="resultLink"></div>').append($('<a href="' + data.law + '" target="_blank">Законодательная база(на испанском языке), требования, списки документов</a>'));

	results.append(lawLink);

	var appLink = $('<div class="resultLink"></div>').append($('<a class="resultLink" href="' + data.application + '" target="_blank">Бланк заявления на регистрацию</a>'));

	results.append(appLink);

	var placeLink = $('<div class="resultLink"></div>');

	placeLink.append($('<h5>Где регистрируют:</h5>'));
	placeLink.append($('<span>' + data.where + '</span>'))

	results.append(placeLink);

	var closeButton = $('<button class="btn btn-primary btn-close">Закрыть</button>');

	closeButton.click(
		function () {
			results.remove();
		}
	);

	results.append(closeButton);

	$('body').append(results);
};

$(document).ready(
	function() {
		$('.comunidad').on(
			'click',
			function(event) {
				var community = event.target.getAttribute('data-community');

				Spain.showData(community);
			}
		);
	}
);
